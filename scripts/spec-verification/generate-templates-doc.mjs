#!/usr/bin/env node
/**
 * generate-templates-doc.mjs
 *
 * Renders `scripts/spec-verification/COMMAND-TEMPLATES.md` from the
 * registry in `command-templates.mjs`. Re-run whenever templates or the
 * verification-need map change so the docs never drift.
 */
import { writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { COMMAND_TEMPLATES, VERIFICATION_NEED_MAP, listByCategory } from "./command-templates.mjs";
import { FOLDER_PROFILES } from "./profiles.mjs";

const HERE = dirname(fileURLToPath(import.meta.url));
const OUT = join(HERE, "COMMAND-TEMPLATES.md");
const TODAY = new Date().toISOString().slice(0, 10);

function categoryHeading(cat) {
  return ({
    lint: "🧹 Lint", test: "🧪 Test", schema: "🗄️ Schema",
    build: "🏗️ Build", security: "🔒 Security", meta: "🧭 Meta (composites)",
  })[cat] || cat;
}

function templatesSection() {
  const groups = listByCategory();
  const order = ["lint", "test", "schema", "build", "security", "meta"];
  const lines = [];
  for (const cat of order) {
    const items = groups[cat];
    if (!items) continue;
    lines.push(`### ${categoryHeading(cat)}`, "");
    lines.push("| Key | Asserts | Command |");
    lines.push("|-----|---------|---------|");
    for (const t of items.sort((a, b) => a.key.localeCompare(b.key))) {
      const cmd = "`" + t.command.replace(/\|/g, "\\|") + "`";
      lines.push(`| \`${t.key}\` | ${t.asserts} | ${cmd} |`);
    }
    lines.push("");
  }
  return lines.join("\n");
}

function needMapSection() {
  const lines = ["| Verification need | Template key | Command |", "|-------------------|--------------|---------|"];
  for (const [need, key] of Object.entries(VERIFICATION_NEED_MAP)) {
    const tpl = COMMAND_TEMPLATES[key];
    const cmd = tpl ? "`" + tpl.command.replace(/\|/g, "\\|") + "`" : "_(missing)_";
    lines.push(`| ${need} | \`${key}\` | ${cmd} |`);
  }
  return lines.join("\n");
}

function profileUsageSection() {
  const lines = ["| Folder | AC tag | Templates used |", "|--------|--------|----------------|"];
  for (const [folder, p] of Object.entries(FOLDER_PROFILES).sort()) {
    const keys = p.commandKeys.map((k) => `\`${k}\``).join(", ");
    lines.push(`| \`${folder}\` | \`${p.tag}\` | ${keys} |`);
  }
  return lines.join("\n");
}

const md = `# Verification Command Templates

**Generated:** ${TODAY}

Single source of truth for the shell commands used in every
\`## Verification\` block injected by
\`scripts/spec-verification/inject-verification-sections.mjs\`. Edit the
registry in \`command-templates.mjs\`, then run
\`npm run spec:verify:docs\` to refresh this file.

---

## 1. Verification needs → templates

The recommended template for each common verification need:

${needMapSection()}

---

## 2. Template registry

${templatesSection()}

---

## 3. Where each template is used

Folder profiles (in \`profiles.mjs\`) reference template keys via the
\`commandKeys\` field. The injector composes them with \`&&\`:

${profileUsageSection()}

---

## 4. Adding a template

1. Add an entry to \`COMMAND_TEMPLATES\` in \`command-templates.mjs\` with a
   stable kebab-case key (e.g. \`lint:new-thing\`).
2. If it answers a recurring need, register it in \`VERIFICATION_NEED_MAP\`.
3. Reference it from any folder profile via \`commandKeys: ["lint:new-thing"]\`.
4. Run \`npm run spec:verify:docs && npm run spec:verify:inject\`.

_Generated by \`scripts/spec-verification/generate-templates-doc.mjs\`._
`;

writeFileSync(OUT, md, "utf8");
console.log(`OK Wrote ${OUT} (${Object.keys(COMMAND_TEMPLATES).length} templates, ${Object.keys(FOLDER_PROFILES).length} profiles)`);