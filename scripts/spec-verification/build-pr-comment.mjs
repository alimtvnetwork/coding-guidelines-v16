#!/usr/bin/env node
/**
 * build-pr-comment.mjs
 *
 * Reads the JSON coverage report produced by
 * ``generate-coverage-report.mjs`` and renders a Markdown body suitable
 * for a sticky PR comment posted by
 * ``marocchino/sticky-pull-request-comment``.
 *
 * Usage:
 *   node scripts/spec-verification/build-pr-comment.mjs <input.json> <output.md>
 *
 * Kept separate from the workflow YAML so the comment template is
 * version-controlled prose (easy to diff, easy to test locally) instead
 * of an inline heredoc inside CI config.
 */
import { readFileSync, writeFileSync } from "node:fs";

const MAX_MISSING_LISTED = 50;
const MAX_STRAY_LISTED = 20;

function requireArgs(argv) {
  const input = argv[2];
  const output = argv[3];
  if (!input || !output) {
    console.error("Usage: build-pr-comment.mjs <input.json> <output.md>");
    process.exit(2);
  }
  return { input, output };
}

function loadReport(path) {
  try { return JSON.parse(readFileSync(path, "utf8")); }
  catch (e) {
    console.error(`::error::cannot read ${path}: ${e.message}`);
    process.exit(1);
  }
}

function statusLine(missingCount) {
  if (missingCount === 0) return "✅ **All expected files have a `## Verification` block.**";
  return "❌ **Missing `## Verification` blocks detected.**";
}

function shortSha() {
  const sha = process.env.GITHUB_SHA || "";
  return sha.slice(0, 7) || "unknown";
}

function renderHeader(summary) {
  const lines = [];
  lines.push("<!-- spec-verification-coverage -->");
  lines.push("## 📋 Spec Verification Coverage");
  lines.push("");
  lines.push(statusLine(summary.totals.missing));
  lines.push("");
  lines.push(`- **Mode:** \`${summary.mode}\``);
  lines.push(`- **Coverage:** ${summary.totals.updated} / ${summary.totals.expected} expected files (${summary.coveragePct}%)`);
  lines.push(`- **Missing:** ${summary.totals.missing}`);
  lines.push(`- **Stray blocks:** ${summary.totals.stray}`);
  lines.push(`- **Commit:** \`${shortSha()}\``);
  lines.push("");
  return lines;
}

function renderMissingSection(missing) {
  if (!missing.length) return [];
  const lines = [];
  lines.push("### Files still missing `## Verification`");
  lines.push("");
  const shown = missing.slice(0, MAX_MISSING_LISTED);
  for (const f of shown) lines.push(`- \`spec/${f}\``);
  const overflow = missing.length - shown.length;
  if (overflow > 0) {
    lines.push("");
    lines.push(`_…and ${overflow} more. See the uploaded \`spec-verification-coverage\` artifact for the full list._`);
  }
  lines.push("");
  lines.push("**Fix:** run `npm run spec:verify:inject` locally, then commit the updated overviews.");
  lines.push("");
  return lines;
}

function renderStraySection(stray) {
  if (!stray.length) return [];
  const lines = [];
  lines.push("### Stray `## Verification` blocks (out of scope under current mode)");
  lines.push("");
  const shown = stray.slice(0, MAX_STRAY_LISTED);
  for (const f of shown) lines.push(`- \`spec/${f}\``);
  const overflow = stray.length - shown.length;
  if (overflow > 0) lines.push(`_…and ${overflow} more._`);
  lines.push("");
  lines.push("**Fix:** `npm run spec:verify:inject -- --strip` or switch the mode to `--mode all-files`.");
  lines.push("");
  return lines;
}

function renderFooter() {
  return [
    "_Auto-generated by `.github/workflows/spec-verification-coverage.yml`. This comment is updated in place on every push._",
  ];
}

function buildBody(report) {
  const { summary, buckets } = report;
  return [
    ...renderHeader(summary),
    ...renderMissingSection(buckets.missing || []),
    ...renderStraySection(buckets.stray || []),
    ...renderFooter(),
  ].join("\n");
}

function main() {
  const { input, output } = requireArgs(process.argv);
  const report = loadReport(input);
  const body = buildBody(report);
  try { writeFileSync(output, body + "\n", "utf8"); }
  catch (e) {
    console.error(`::error::cannot write ${output}: ${e.message}`);
    process.exit(1);
  }
  console.log(`Wrote ${output} (${body.length} bytes)`);
}

main();